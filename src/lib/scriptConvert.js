function replaceMultiCharTokens(text, map) {
  return text
    .replace(/क्ष/g, map['क्ष'] || 'क्ष')
    .replace(/ज्ञ/g, map['ज्ञ'] || 'ज्ञ')
    .replace(/ॐ/g, map['ॐ'] || 'ॐ');
}

function convertWithMap(text, map) {
  if (!text) return '';

  const prepared = replaceMultiCharTokens(text, map);
  let result = '';

  for (const ch of prepared) {
    result += map[ch] ?? ch;
  }

  return result;
}

export function convertDevanagariToScript(text, targetLang) {
  if (!text) return '';

  if (targetLang === 'od') {
    return convertWithMap(text, DEVANAGARI_TO_ODIA);
  }

  if (targetLang === 'te') {
    return convertWithMap(text, DEVANAGARI_TO_TELUGU);
  }

  return text;
}